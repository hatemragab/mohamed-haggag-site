// استخراج مُعرّف يوتيوب من رابط كامل أو إرجاع المُعرّف كما هو
function ytId(linkOrId) {
  if (!linkOrId) return "";
  const m = String(linkOrId).match(/(?:youtu\.be\/|v=|embed\/|shorts\/)([\w-]{11})/);
  return m ? m[1] : linkOrId;
}
// صورة مصغّرة لفيديو يوتيوب (تعمل مع الفيديوهات غير المُدرجة أيضاً)
function ytThumb(linkOrId, q = "mqdefault") {
  const id = ytId(linkOrId);
  return /^[\w-]{11}$/.test(id) ? `https://i.ytimg.com/vi/${id}/${q}.jpg` : "";
}

// مولّد دروس لكل ساب-كاتيجوري (deterministic)
const LESSON_TEMPLATES = [
  "مدخل وتمهيد",
  "القاعدة الأساسية",
  "أمثلة وتطبيقات",
  "تدريبات محلولة",
  "أخطاء شائعة وتصحيحها",
  "مراجعة شاملة",
  "اختبار قصير",
  "إثراء وتوسّع"
];
function genLessons(catId, subId, subTitle) {
  // تجاوز الأدمن إن وُجد
  const key = catId + "|" + subId;
  if (window.__lessonsOverride && window.__lessonsOverride[key]) return window.__lessonsOverride[key];
  // عدد الدروس يعتمد على hash بسيط
  const seed = (catId.length + subId.length + subTitle.length);
  const count = 5 + (seed % 4); // 5..8
  const list = [];
  for (let i = 0; i < count; i++) {
    const mins = 12 + ((seed + i * 7) % 28); // 12..40 دقيقة
    list.push({
      id: `${catId}-${subId}-${i}`,
      idx: i + 1,
      title: `${LESSON_TEMPLATES[i % LESSON_TEMPLATES.length]}`,
      sub: subTitle,
      duration: mins,
      youtube: DEMO_YT, // ضع هنا رابط/مُعرّف فيديو الدرس على يوتيوب (unlisted)
      free: i === 0 // الدرس الأول تجريبي مجاني
    });
  }
  return list;
}

