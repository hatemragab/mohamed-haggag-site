export interface SeedLesson {
  title: string;
  youtubeId: string;
  durationMinutes: number;
  free: boolean;
  order: number;
}

/** Deterministic lesson generator — exact replica of the prototype's genLessons(). */
export function genLessons(
  catSlug: string,
  subKey: string,
  levelTitle: string,
): SeedLesson[] {
  const seed = catSlug.length + subKey.length + levelTitle.length;
  const count = 5 + (seed % 4); // 5..8
  return Array.from({ length: count }, (_, i) => ({
    title: LESSON_TEMPLATES[i % LESSON_TEMPLATES.length],
    youtubeId: DEMO_YT,
    durationMinutes: 12 + ((seed + i * 7) % 28), // 12..40
    free: i === 0,
    order: i + 1,
  }));
}

